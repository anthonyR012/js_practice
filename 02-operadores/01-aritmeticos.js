
let a = 5;
let b = 7;


console.log(a + b); // Suma
console.log(a - b); // Resta
console.log(a * b); // Multiplicación
console.log(a / b); // División
console.log(a % b); // Módulo
console.log(a ** b); // Exponenciación
console.log(a++); // Incremento (postfijo)
console.log(++a); // Incremento (prefijo)
console.log(a--); // Decremento (postfijo)
console.log(--a); // Decremento (prefijo)
console.log(a += b); // Suma y asignación
console.log(a -= b); // Resta y asignación
console.log(a *= b); // Multiplicación y asignación
console.log(a /= b); // División y asignación
console.log(a %= b); // Módulo y asignación
console.log(a **= b); // Exponenciación y asignación
